    /***
     * Load navigation menu header information
     * like background image, profile image
     * name, website, notifications action view (dot)
     */
    private void loadNavHeader() {
        //call api
        try{
            OkHttpClient client = new OkHttpClient();
            Request request = new Request.Builder()
                    .url(getText(R.string.profileRequest).toString())
                    .build();
            Response response = client.newCall(request).execute();
            String result = response.body().toString();
            Gson gson = new GsonBuilder().create();
            ImageView profilePicture = (ImageView) findViewById(R.id.img_profile);
            User profile = new User();
            profile = gson.fromJson(result,User.class);

            //set layout attribute
            new ImageLoadTask(profile.getProfilePicture(), profilePicture).execute();

            // name, website
            txtName.setText(profile.getName());
        }
        catch (Exception e){
            Toast.makeText(this,e.getMessage(),Toast.LENGTH_SHORT);
        }

        // loading header background image
        Glide.with(this).load(urlNavHeaderBg)
                .crossFade()
                .diskCacheStrategy(DiskCacheStrategy.ALL)
                .into(imgNavHeaderBg);

        // Loading profile image
        Glide.with(this).load(urlProfileImg)
                .crossFade()
                .thumbnail(0.5f)
                .bitmapTransform(new CircleTransform(this))
                .diskCacheStrategy(DiskCacheStrategy.ALL)
                .into(imgProfile);

        // showing dot next to notifications label
        navigationView.getMenu().getItem(3).setActionView(R.layout.menu_dot);
    }
